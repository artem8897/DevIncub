package by.bsu.finalproject.exception;

public class FIleDataReaderException extends Exception {
    public FIleDataReaderException() {
    }

    public FIleDataReaderException(String message) {
        super(message);
    }

    public FIleDataReaderException(String message, Throwable cause) {
        super(message, cause);
    }

    public FIleDataReaderException(Throwable cause) {
        super(cause);
    }
}
